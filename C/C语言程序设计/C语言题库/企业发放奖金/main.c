#include <stdio.h>

int main()
{
    long int lr,bonus;
    printf("Please Input lr\n");
    scanf("%d",&lr);
    if(lr<=0)
    {
        bonus=0;
    }
    if(lr>0&&lr<=100000)
    {
        bonus=lr*0.1;
    }
    if(lr>100000&&lr<=200000)
    {
        bonus=100000*0.1+(lr-100000)*0.075;
    }
    if(lr>200000&&lr<=400000)
    {
        bonus=100000*0.1+100000*0.075+(lr-200000)*0.05;
    }
    if(lr>400000&&lr<=600000)
    {
        bonus=100000*0.1+100000*0.075+200000*0.05+(lr-400000)*0.03;
    }
    if(lr>600000&&lr<=1000000)
    {
        bonus=100000*0.1+100000*0.075+200000*0.05+200000*0.03+(lr-600000)*0.015;
    }
    if(lr>1000000)
    {
        bonus=100000*0.1+100000*0.075+200000*0.05+200000*0.03+400000*0.015+(lr-1000000)*0.01;
    }
    printf("Output:\nbonus=%d",bonus);
    return 0;
}
